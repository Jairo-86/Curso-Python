# Primer ejercicio
print("¿Para que el resultado sea 50, cuál debe ser el número que falta?")
print("25 se debe multiplicar por? ")
multiplicar = float(input(""))  # Convertimos la entrada a un número decimal (float)
resultado = 25 * multiplicar  # Operación de multiplicación

# Mostrar el resultado
print("Si multiplicas " + str(multiplicar) + " por 25 el resultado es " + str(resultado))

